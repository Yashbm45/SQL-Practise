

-- ************* ************* ************* Common SQL Clauses & Their Roles ************* ************* *************
-- SELECT		Specifies which columns or expressions to return in the result set.
-- FROM			Defines the table(s) to retrieve data from.
-- JOIN			Combines rows from two or more tables based on a related column.
-- WHERE		Filters rows before grouping, based on conditions.
-- GROUP BY		Groups rows sharing a common value, used with aggregate functions.
-- HAVING		Filters groups after GROUP BY, based on aggregate conditions.
-- ORDER BY		Sorts the final result set by one or more columns or expressions.
-- LIMIT		Restricts the number of rows returned in the result.
-- OFFSET		Skips a specified number of rows before starting to return rows.
-- DISTINCT		Removes duplicate rows from the result set.
-- AS			Renames a column or table (aliasing) for clarity or convenience.

